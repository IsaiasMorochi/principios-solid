package com.isaias.solid.v2.ocp;

public class Main {

    /**
     * 2do Principle
     * OCP (Open Close Principle)
     * 1- Las entidades de software (clases, modulos, funciones),
     *    deberian ser abiertos para la extension pero cerrados
     *    para la modificacion.
     */

    /**
     * Especificacion
     * 1- Diseña un descuento para lanzar nuestra app.
     * 2- Diseña un manager de descuento para gestionar el cupon.
     * 3- Diseña un descuento para clientes Gold.
     * 4- Diseña un descuento para BlackFriday.
     * 5- Diseña un descuento para Navidad.
     */

    /**
     * Nota
     * Las nuevas caracteristicas no deberian afectar a los modulos que
     * ya existen el software deberia ser extendible a nuevas caracteristicas.
     */
}
